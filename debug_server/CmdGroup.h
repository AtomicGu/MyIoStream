#pragma once
#include "debug_server.h"



//==================================================================================================
// 类名：CmdGroup
// 说明：
//		组命令类提供一种对命令进行分组的简易方式
//		利用继承，该类可以方便地实现“主命令 子命令”这样的命令模式
//==================================================================================================
class CmdGroup :public DebugCmd
{
public:
	//==============================================================================================
	// 函数：operator()
	// 参数：
	//		in - 参数输入流
	//		out - 命令使用的输出流
	//
	// 返回：子命令的返回值，若指令无效，则返回true
	// 说明：这个函数是对DebugCmd接口的实现
	//==============================================================================================
	virtual bool operator()(ICharStream& in, OCharStream out);

protected:
	DebugCmdMap _cmdMap;
};
